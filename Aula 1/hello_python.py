print("Hello, Python!")

# Definindo variáveis
nome = "Alice"
idade = 25

# Imprimindo informações
print(f"Olá, {nome}! Você tem {idade} anos.")

# Realizando operações
idade_no_futuro = idade + 5
print(f"Daqui a 5 anos, você terá {idade_no_futuro} anos.")

# Criando uma lista
frutas = ["maçã", "banana", "uva"]

# Iterando sobre a lista
for fruta in frutas:
    print(fruta)


# Definindo uma função
def saudacao(nome):
    return f"Olá, {nome}!"

# Chamando a função
mensagem = saudacao("Carlos")
print(mensagem)


# Concatenando strings
primeiro_nome = "João"
ultimo_nome = "Silva"
nome_completo = primeiro_nome + " " + ultimo_nome

# Imprimindo o nome completo
print("Nome completo:", nome_completo)

# Transformando para letras maiúsculas
print("Nome em maiúsculas:", nome_completo.upper())


